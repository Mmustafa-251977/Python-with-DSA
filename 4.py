# Data Types and variable and arithmatic operation 
# In Python, data types are used to define the type of data that a variable can hold.
# Python has several built-in data types, including:
# 1. Numeric Types: int, float, complex
# 2. Sequence Types: list, tuple, range
# 3. Text Type: str
# 4. Set Types: set, frozenset
# 5. Mapping Type: dict
# 6. Boolean Type: bool
# 7. Binary Types: bytes, bytearray, memoryview
# Arithmatic Operations in Python
# Python supports various arithmetic operations, including addition, subtraction, multiplication, and division.

# Example of Arithmetic Operations
# Addition: x + y
# Subtraction: x - y
# Multiplication: x * y
# Division: x / y

#1.Numeric data types 
Num1 = 20 # This is the int data types 
Num2 = 30
print(f"This is number one : {Num1}")
print(f"This is number two : {Num2}")
print(f"The Addition of the {Num1} and {Num2} is : ", Num1 + Num2)
print(f"The Subtraction of the {Num1} and {Num2} is : ", Num1 - Num2)
print(f"The  Multiplication of the {Num1} and {Num2} is : ", Num1 * Num2)
print(f"The Division of the {Num1} and {Num2} is :  {Num1 / Num2:.2f} ")

print("---------------------------------------------------")

Num3 = 2.25 # this floating point Number 
